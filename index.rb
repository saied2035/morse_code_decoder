
def decode_char(morse_char)
morse_code_hash = {  
	'.-' => 'A' ,
	'-...' => 'B',
	'-.-.' => 'C',
	'-..' => 'D',
	'.' => 'E',
	'..-.' => 'F',
	'--.' => 'G',
	'....' => 'H',
	'..' => 'I',
	'.---' => 'J',
	'-.-' => 'K',
	'.-..' => 'L',
	'--' => 'M',
	'---' => 'O',
	'.--.' => 'P',
	'--.-' => 'Q',
	'.-.' => 'R',
	'...' => 'S',
	'-' => 'T',
	'..-' => 'U',
	'...-' => 'V',
	'.--' => 'W',
	'-..-' => 'X',
	'-.--' => 'Y',
	'--..' => 'Z'
}	
return morse_code_hash[morse_char]
end 